public class AutoBoxingAndAutoUnboxing {

	public static void main(String[] args) {
		
		// AutoBoxing
		
		Integer i = 100;
		
		System.out.println(i);
		
		// AutoUnboxing
		
		int b = i;
		
		System.out.println(b);

	}

}
