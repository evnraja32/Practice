package oops.codingbasics;

public class SumOfTwoNumbers {

	public static void main(String[] args) {
		// variables decleration
		int x, y;

		// store values intovariables
		x = 10;
		y = 25;

		// calculate sum and store result into z
		int z = x + y;

		// display result
		System.out.println(z);
	}
}
